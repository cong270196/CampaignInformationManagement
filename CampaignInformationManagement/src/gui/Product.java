    private void loadTableProduct(List<Products> readAll) {
        Vector column = new Vector();
        column.add("ID");
        column.add("NAME");
        column.add("DESCRIPTION");
        Vector rows = new Vector();
        for (Products pp : readAll) {
            Vector row = new Vector();
            row.add(pp.getProductid());
            row.add(pp.getProductname());
            row.add(pp.getDescription());
            rows.add(row);
        }
        tbProduct.setModel(new DefaultTableModel(rows, column));
        tbProduct.updateUI();
        spProduct.setViewportView(this.tbProduct);
    }
