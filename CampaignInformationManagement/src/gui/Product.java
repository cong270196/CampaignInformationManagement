    private void tbProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProductMouseClicked
        // TODO add your handling code here:
        rowSelected = tbProduct.getSelectedRow();
        String productid = (String) tbProduct.getValueAt(rowSelected, 0);
        String productname = (String) tbProduct.getValueAt(rowSelected, 1);
        String description = (String) tbProduct.getValueAt(rowSelected, 2);

        tfProductid.setText(productid);
        tfProductid.setEditable(false);
        tfProductname.setText(productname);
        tfDescription.setText(description);
    }//GEN-LAST:event_tbProductMouseClicked

    private void btCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCreateActionPerformed
        // TODO add your handling code here:
        if (tfProductid.getText().trim().equals("") || tfProductname.getText().trim().equals("") || tfDescription.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please complete all information");
        } else {
            String productid = tfProductid.getText().trim();
            String productname = tfProductname.getText().trim();
            String description = tfDescription.getText().trim();
            Products p = new Products(productid, productname, description);
            if (dao.readByProductid(productid) != null) {
                JOptionPane.showMessageDialog(null, "ID already exists");
            } else if (dao.create(p) != null) {
                JOptionPane.showMessageDialog(null, "Success");
                loadTableProduct(dao.readAll());
                tfProductid.setText("");
                tfProductname.setText("");
                tfDescription.setText("");
                rowSelected = -1;
            }
        }
    }//GEN-LAST:event_btCreateActionPerformed

