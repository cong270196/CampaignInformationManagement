    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
        if (rowSelected == -1) {
            JOptionPane.showMessageDialog(null, "Please select the row");
        } else if (tfProductname.getText().trim().equals("") || tfDescription.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please complete all information");
        } else {
            String productid = (String) tbProduct.getValueAt(rowSelected, 0);
            String productname = tfProductname.getText().trim();
            String description = tfDescription.getText().trim();
            Products p = new Products(productid, productname, description);
            if (dao.update(p) != null) {
                JOptionPane.showMessageDialog(null, "Success");
                loadTableProduct(dao.readAll());
                tfProductid.setEditable(true);
                tfProductid.setText("");
                tfProductname.setText("");
                tfDescription.setText("");
                rowSelected = -1;
            }
        }
    }//GEN-LAST:event_btUpdateActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        if (rowSelected == -1) {
            JOptionPane.showMessageDialog(null, "Please select the row");
        } else {
            String productid = (String) tbProduct.getValueAt(rowSelected, 0);
            if (dao.delete(productid)) {
                JOptionPane.showMessageDialog(null, "Success");
                loadTableProduct(dao.readAll());
                tfProductid.setEditable(true);
                tfProductid.setText("");
                tfProductname.setText("");
                tfDescription.setText("");
                rowSelected = -1;
            }
        }
    }//GEN-LAST:event_btDeleteActionPerformed


