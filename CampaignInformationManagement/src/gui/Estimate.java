    private void btApplyEstimateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btApplyEstimateActionPerformed
        // TODO add your handling code here:
        if (tfBudget.getText().trim().equals("") || tfQuantity.getText().trim().equals("") || tfVarianceAllowed.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please complete all information");
        } else {
            int budget = Integer.parseInt(tfBudget.getText().trim());
            int target = Integer.parseInt(tfQuantity.getText().trim());
            int varianceallowed = Integer.parseInt(tfVarianceAllowed.getText().trim());
            Estimates e = new Estimates(campaignid, budget, target, varianceallowed);
            if (estimatedao.update(e) != null) {
                JOptionPane.showMessageDialog(null, "Success");
                tfBudget.setText("");
                tfQuantity.setText("");
                tfVarianceAllowed.setText("");
            }
        }
    }//GEN-LAST:event_btApplyEstimateActionPerformed


