    private void btApplyBudgetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btApplyBudgetActionPerformed
        if (tfRealityBudget.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please complete all information");
        } else {
            int budget = Integer.parseInt(tfRealityBudget.getText().trim());
            for (Realitys rr : realitydao.readAll()) {
                if (rr.getCampaignid().equals(campaignid)) {
                    Realitys r = new Realitys(rr.getCampaignid(), rr.getSumquantity(), rr.getSumbudget() + budget);
                    realitydao.update(r);
                }
            }
            JOptionPane.showMessageDialog(null, "Success");
            tfRealityBudget.setText("");
        }
    }//GEN-LAST:event_btApplyBudgetActionPerformed

